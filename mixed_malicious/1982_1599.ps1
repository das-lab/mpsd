                    [int]$secsleft2 = 0
                }
                $pctcomp1 = $total1 / $allbytes * 100
                [int]$secselapsed1 = [int]($sw1.elapsedmilliseconds.ToString()) / 1000
                if ($pctcomp1 -gt 0) {
                    [int]$secsleft1 = $secselapsed1 / $pctcomp1 * 100 - $secselapsed1
